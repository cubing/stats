<p>This website collects statistics based on the <a href="http://worldcubeassociation.org/results/">WCA results database</a>. All statistics use php and MySQL queries on the latest WCA results database export.</p>

<p>Also see the <a href="http://www.speedsolving.com/forum/showthread.php?34250-WCA-Statistics-Website">announcement on Speedsolving.com</a>.</p>

<h2>Goals</h2>
<ul>
  <li>(unofficial) updated statistics on one website</li>
  <li>user-submitted statistics code (php and MySQL)</li>
</ul>

<h2>Why do we need this website?</h2>
<p>Official statistics can be found on the <a href="http://worldcubeassociation.org/results/statistics.php">WCA Statistics page</a>. Although popular statistics may be incorporated into the official WCA Statistics page, some statistics are inherently unofficial, e.g. different proposed global ranking systems. The hope, moreover, is to encourage cubers, especially those without server access, to learn and experiment with php and MySQL.</p>

